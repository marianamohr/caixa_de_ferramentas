// indexOf()
// Retorna a posição no array se encontrar o valor, caso negativo, retorna -1

const num = [1, 2, 35, 4, 5, 46, 7, 8];

console.log(num.indexOf(1));  // returns 0
console.log(num.indexOf(80)); // returns -1
console.log(num.indexOf(46)); // returns 5
console.log(num.indexOf(35)); // returns 2
